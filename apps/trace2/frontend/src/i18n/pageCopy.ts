const pageTranslations: Partial<Record<LanguageCode, Partial<Copy>>> = {
  fr,
  es,
  de,
  ja,
  "zh-Hans": zhHans,
  "zh-Hant": zhHant,
  pt,
  id,
  ms,
  ga,
  pl,
  nl,
};

function deepMerge<T extends Record<string, any>>(base: T, override?: Partial<T>): T {
  if (!override) return base;
  const merged: Record<string, any> = { ...base };
  for (const [key, value] of Object.entries(override)) {
    merged[key] = value && typeof value === "object" && !Array.isArray(value)
      ? deepMerge(base[key] ?? {}, value as Record<string, any>)
      : value;
  }
  return merged as T;
}

export function traceCopy(language: LanguageCode): Copy {
  return deepMerge(en, pageTranslations[language] as Partial<Copy> | undefined);
}

export function template(text: string, values: Record<string, string | number>): string {
  return text.replace(/\{\{\s*(\w+)\s*\}\}/g, (_match, key) => String(values[key] ?? ""));
}

export function plural(one: string, many: string, count: number): string {
  return template(count === 1 ? one : many, { count });
}
